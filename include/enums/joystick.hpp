#ifndef ENGINE_JOYSTICK_HPP
#define ENGINE_JOYSTICK_HPP

/**
 * @brief Enumeration for different joystick buttons.
 */
enum JoystickButton {
    AButton,
    BButton,
    XButton,
    YButton,
    LeftButton,
    RightButton,
};

/**
 * @brief Enumeration for different joystick axis.
 */
enum JoystickAxis {
    LeftJoystickX,
    LeftJoystickY,
    RightJoystickX,
    RightJoystickY,
    LeftTrigger,
    RightTrigger,
    LeftTouchpadX,
    LeftTouchpadY,
    RightTouchpadX,
    RightTouchpadY,
};

#endif //ENGINE_JOYSTICK_HPP
